package Java_AssignTwo_Variable_Class_Arith;

public class unary_Operator_Ex {
    public static void main(String[] args) {
        int a=20;
        System.out.println("Initial value is "+a);
        System.out.println("Increment before "+ ++a);
        System.out.println("Here is the output of increement before:========"+a);

        System.out.println("Increemnt after"+ a++);
        System.out.println("Here is output of increment after"+a);

        System.out.println("Drecement before example"+--a);
        System.out.println("Output of decrement before"+a);

        System.out.println("Decremtn of after"+ --a);
        System.out.println("Decreemnt after output"+a);




    }


}
